    public void showRecords() {
        DefaultTableModel tblModel = (DefaultTableModel) subjForm_Table.getModel();
        tblModel.setRowCount(0);
        
        TrinidadEnrollmentSystem main = new TrinidadEnrollmentSystem();
        main.DBConnect();
        String query = "select * from Subjects;";
        try {
            main.rs = main.st.executeQuery(query);
            while (main.rs.next()) {
                String a = main.rs.getString("subjid");
                String b = main.rs.getString("subjcode");
                String c = main.rs.getString("subjdesc");
                String d = main.rs.getString("subjunits");
                String e = main.rs.getString("subjsched");
                String[] item = {a,b,c,d,e};
                tblModel.addRow(item);
            }
        } catch (Exception ex) {
            System.out.println("FAILED!" + ex);
        }
    }
    
  
private void ShowClassList(int subjID){
    DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
    tblModel.setRowCount(0);

    String query = "SELECT st.studid, st.studname, st.studadd, st.studcontact, st.studgender, st.yrlvl " +
                   "FROM Enroll e " +
                   "JOIN Students st ON e.studid = st.studid " + 
                   "WHERE e.subjid = " + subjID;               
    try {
        TrinidadEnrollmentSystem.rs = TrinidadEnrollmentSystem.st.executeQuery(query);
        while (TrinidadEnrollmentSystem.rs.next()) {
            String[] item = {
                TrinidadEnrollmentSystem.rs.getString("studid"),
                TrinidadEnrollmentSystem.rs.getString("studname"),
                TrinidadEnrollmentSystem.rs.getString("studadd"),
                TrinidadEnrollmentSystem.rs.getString("studcontact"),
                TrinidadEnrollmentSystem.rs.getString("studgender"),
                TrinidadEnrollmentSystem.rs.getString("yrlvl")
            };
            tblModel.addRow(item);
        }
    } catch (Exception ex) {
        System.out.println("Failed to show class list: " + ex);
    }
}
