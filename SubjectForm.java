    private void subjForm_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_subjForm_TableMouseClicked
         int selectedRow = subjForm_Table.getSelectedRow();

    if (selectedRow != -1) {
        String id = subjForm_Table.getValueAt(selectedRow, 0).toString();
        String code = subjForm_Table.getValueAt(selectedRow, 1).toString();
        String description = subjForm_Table.getValueAt(selectedRow, 2).toString();
        String units = subjForm_Table.getValueAt(selectedRow, 3).toString();
        String schedule = subjForm_Table.getValueAt(selectedRow, 4).toString();

        subjID.setText(id);
        subjCode.setText(code);
        subjDescription.setText(description);
        subjUnits.setText(units);
        subjSchedule.setText(schedule);

        ShowClassList(Integer.parseInt(id));

        TrinidadEnrollmentSystem.currentlySelectedSubjectId = Integer.parseInt(id);
    }
    }//GEN-LAST:event_subjForm_TableMouseClicked

